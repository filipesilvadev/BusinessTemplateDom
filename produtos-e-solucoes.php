<script>
    $(document).ready(function() {
        // Inicializa os carrosséis
        $('.owl-carousel').owlCarousel({
            loop: true,
            margin: 10,
            nav: true,
            dots: false,
            items: 4,
            responsiveClass: true,
            responsive: {
                0:{
                  items: 1
                },
                480:{
                  items: 2
                },
                769:{
                  items: 4
                }
            }
        });

        // Função para abrir tabs e fazer scroll
        $('.tab-trigger').on('click', function(e) {
            e.preventDefault();
            
            // Identifica a tab a ser aberta
            var targetTab = $(this).attr('href');

            // Esconde todas as tabs e mostra apenas a escolhida
            $('.tab-content').hide();
            $(targetTab).show();

            // Rola a página até o fim
            $('html, body').animate({
                scrollTop: $(targetTab).offset().top
            }, 800);
        });
    });
</script>
  
